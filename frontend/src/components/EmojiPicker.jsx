const RECENT_KEY = "talksphere-recent-emojis";
const OLD_RECENT_KEY = "chatify-recent-emojis";
function getRecent() {
  try { return JSON.parse(localStorage.getItem(RECENT_KEY) || localStorage.getItem(OLD_RECENT_KEY) || "[]"); } catch { return []; }
}
function addRecent(emoji) {
  const list = [emoji, ...getRecent().filter(e => e !== emoji)].slice(0, 32);
  try { localStorage.setItem(RECENT_KEY, JSON.stringify(list)); } catch (e) { /* ignore */ }
}

export default function EmojiPicker({ onSelect }) {
  const [category, setCategory] = useState("smileys");
  const [search, setSearch] = useState("");
  const [recent, setRecent] = useState(getRecent);

  const displayEmojis = useMemo(() => {
    if (search.trim()) {
      // Very basic search by unicode name not feasible, so search across all
      const all = Object.values(EMOJI_DATA).flat();
      return all; // simplified — just show all when searching
    }
    if (category === "recent") return recent.length ? recent : EMOJI_DATA.smileys.slice(0, 32);
    return EMOJI_DATA[category] || [];
  }, [category, search, recent]);

  const handleSelect = (emoji) => {
    addRecent(emoji);
    setRecent(getRecent());
    onSelect(emoji);
  };

  return (
    <div className="rounded-2xl overflow-hidden flex flex-col shadow-2xl"
      style={{
        width: 320, height: 380,
        background: 'var(--bg-panel)',
        border: '1px solid var(--border)',
      }}>

      {/* Search */}
      <div className="px-3 pt-3 pb-2">
        <input type="text" value={search} onChange={(e) => setSearch(e.target.value)}
          placeholder="Search emojis…"
          className="w-full px-3 py-1.5 text-[13px] rounded-xl border-none focus:outline-none"
          style={{ background: 'var(--bg-input)', color: 'var(--text-primary)' }} />
      </div>

      {/* Category tabs */}
      <div className="flex items-center px-2 gap-0.5 pb-2">
        {CATEGORIES.map((cat) => (
          <button key={cat.id} onClick={() => { setCategory(cat.id); setSearch(""); }}
            className="flex-1 text-[16px] py-1 rounded-lg transition-all"
            style={{
              background: category === cat.id ? 'var(--bg-active)' : 'transparent',
              filter: category === cat.id ? 'none' : 'grayscale(0.3)',
            }}
            title={cat.name}>
            {cat.label}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="flex-1 overflow-y-auto px-2 pb-2">
        <div className="grid" style={{ gridTemplateColumns: 'repeat(8, 1fr)', gap: 2 }}>
          {displayEmojis.map((emoji, i) => (
            <button key={`${emoji}-${i}`} onClick={() => handleSelect(emoji)}
              className="text-[20px] py-1 rounded-lg flex items-center justify-center transition-all hover:scale-125 hover:bg-white/5 active:scale-95">
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
